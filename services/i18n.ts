// the english key if missing so untranslated stuff just shows english
// instead of blowing up. translations are best-effort, corrections welcome.
// TODO: pull device locale as the default instead of hardcoding 'en'

export const LANGUAGES = [
  { code: 'en', label: 'English' },
  { code: 'es', label: 'Español' },
  { code: 'de', label: 'Deutsch' },
  { code: 'fr', label: 'Français' },
  { code: 'zh', label: '中文' },
] as const;

export type LanguageCode = (typeof LANGUAGES)[number]['code'];

let current: LanguageCode = 'en';

export function setLanguage(code: string): void {
  current = (LANGUAGES.some((l) => l.code === code) ? code : 'en') as LanguageCode;
}

export function t(key: string): string {
  if (current === 'en') return key;
  return DICT[current]?.[key] ?? key;
}

